function adaptVeganHeroTextColor() {
  const img = document.getElementById('vdp-main-img');
  const title = document.getElementById('vdp-hero-title');
  if (!img || !title) return;

  const apply = () => {
    try {
      const canvas = document.createElement('canvas');
      const w = Math.max(1, Math.floor(img.naturalWidth * 0.25));
      const h = Math.max(1, Math.floor(img.naturalHeight * 0.25));
      canvas.width = w;
      canvas.height = h;
      const ctx = canvas.getContext('2d');
      if (!ctx) return;
      ctx.drawImage(img, 0, 0, w, h);
      const yStart = Math.floor(h * 0.6);
      const data = ctx.getImageData(0, yStart, w, h - yStart).data;
      let total = 0;
      let count = 0;
      for (let i = 0; i < data.length; i += 4) {
        const r = data[i], g = data[i + 1], b = data[i + 2];
        total += 0.2126 * r + 0.7152 * g + 0.0722 * b;
        count++;
      }
      const brightness = count ? total / count : 0;
      title.classList.remove('light', 'dark');
      title.classList.add(brightness > 150 ? 'dark' : 'light');
    } catch (_) {
      title.classList.remove('dark');
      title.classList.add('light');
    }
  };

  if (img.complete) apply();
  else img.addEventListener('load', apply, { once: true });
}

