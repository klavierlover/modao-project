async function renderForum() {
  const masonry = document.getElementById('forum-masonry');
  if (!masonry) return;

  // 骨架屏
  masonry.innerHTML = `<div class="forum-skeleton">${forumSkeletonHtml()}</div>`;
  masonry.style.display = '';

  // 尝试加载用户投稿（后台 API）
  let ugcPosts = [];
  try {
    const resp = await fetch('/api/user/posts?module=forum&limit=20');
    if (resp.ok) {
      const data = await resp.json();
      ugcPosts = (data.posts || []).map((a, idx) => ({
        id:          `ugc-${a.slug}`,
        author:      (a.tags || []).find(t => t !== 'ugc' && !['修法心得','经典讨论','朝圣分享','素食推荐','问道解惑','生活禅','同修分享'].includes(t)) || '同修',
        avatar:      '',
        time:        '最近发布',
        section:     (a.tags || []).find(t => ['修法心得','经典讨论','朝圣分享','素食推荐','问道解惑','生活禅','同修分享'].includes(t)) || '同修分享',
        title:       a.title,
        cover:       a.cover_url || '',
        coverHeight: 240,
        excerpt:     a.summary || '',
        articleUrl:  '',
        likes:       0, comments: 0, collects: 0,
        tags:        a.tags || [],
      }));
    }
  } catch (_) { /* 网络失败时静默处理 */ }

  // 合并硬编码 + 用户投稿
  const allPosts = [...ugcPosts, ...(window.FORUM_POSTS || [])];

  // 分类筛选
  const activeChip = document.querySelector('.forum-filter-bar .chip.active');
  const category = activeChip?.textContent?.trim() || '全部';
  const filtered = category === '全部' ? allPosts : allPosts.filter(p => p.section === category);

  // 排序
  const sorted = _forumSort === 'hot'
    ? [...filtered].sort((a, b) => (b.likes || 0) - (a.likes || 0))
    : filtered;

  if (!sorted.length) {
    masonry.innerHTML = `<div style="text-align:center;padding:60px;color:var(--ink-40)">该分类暂无帖子，来发第一篇吧 ✍️</div>`;
    return;
  }

  masonry.innerHTML = sorted.map(buildForumCard).join('');
}

