async function loadPublishedContent() {
  try {
    const resp = await fetch('/api/content/published');
    if (!resp.ok) return;
    const data = await resp.json();
    if (!data?.ok || !data?.modules) return;
    let changed = false;
    Object.values(data.modules).forEach(entry => {
      const prevVersion = App.publishedVersionMap[entry.module_key];
      App.publishedVersionMap[entry.module_key] = entry.id;
      if (prevVersion && prevVersion !== entry.id) changed = true;
      applyPublishedSnapshot(entry.module_key, entry.snapshot || {});
    });
    if (changed) {
      showPage(App.currentPage);
      showToast('检测到最新发布内容，页面已更新', 1800);
    }
  } catch (_err) {
    // Keep local defaults as fallback.
  }
}

function applyPublishedSnapshot(moduleKey, snapshot) {
  const rootPayload = snapshot?.blocks?.find?.(b => b.block_key === 'module_root')?.payload || {};
  const draftArticles = snapshot?.articles || [];

  if (moduleKey === 'forum') {
    if (Array.isArray(rootPayload.posts)) window.FORUM_POSTS = rootPayload.posts;
    else if (draftArticles.length) {
      window.FORUM_POSTS = draftArticles.map((a, idx) => ({
        id: idx + 1,
        author: a.author || '莫道编辑部',
        avatar: a.avatar || './assets/images/logo-lotus.svg',
        time: '刚刚更新',
        section: a.section || '同修精选',
        title: a.title,
        cover: a.cover_url || '/assets/images/home-hero-larunggar.jpg',
        coverHeight: 320,
        excerpt: a.summary || '',
        likes: Number(a.likes || 0),
        comments: Number(a.comments || 0),
        collects: Number(a.collects || 0),
        articleUrl: a.article_url || '',
      }));
    }
  }

  // 过滤 visible:false 的内容（后台下线控制）
  const onlyVisible = arr => (arr || []).filter(item => item.visible !== false);

  if (moduleKey === 'pilgrimage' && Array.isArray(rootPayload.sites)) {
    window.PILGRIMAGE_SITES = onlyVisible(rootPayload.sites);
  }

  if (moduleKey === 'library' && Array.isArray(rootPayload.sutras)) {
    window.SUTRAS = onlyVisible(rootPayload.sutras);
  }

  if (moduleKey === 'vegan') {
    if (Array.isArray(rootPayload.restaurants)) window.WUHAN_RESTAURANTS = onlyVisible(rootPayload.restaurants);
    if (Array.isArray(rootPayload.recipes))     window.VEGAN_RECIPES     = onlyVisible(rootPayload.recipes);
  }

  if (moduleKey === 'companion') {
    if (Array.isArray(rootPayload.companions)) window.COMPANIONS = onlyVisible(rootPayload.companions);
  }
}

